# bin/lib-caddy.sh — shared Caddy publishing for little-living-apps. Sourced (not executed) by
# bootstrap.sh and bin/new-instance. Every app — the primary AND each bin/new-instance app — is
# published the SAME way: its own site block at /etc/caddy/sites/<name>.caddy, imported by the top
# /etc/caddy/Caddyfile. The top file owns no site of its own, so regenerating it never clobbers an
# instance, and there is no special-cased "primary" Caddy config.

# lila_write_caddy_site NAME DOMAIN APP_PORT INSPECTOR_PORT
# Writes the instance's site block, ensures the top Caddyfile imports the sites dir, then validates
# and reloads Caddy. Returns non-zero on an empty domain or invalid config (caller should die). If
# caddy isn't installed yet it warns and returns 0 (the block is written, served once Caddy is up).
lila_write_caddy_site() {
  local name="$1" domain="$2" app_port="$3" inspector_port="$4"
  [[ -n "$domain" ]] || { echo "lila_write_caddy_site: empty domain for instance '$name'" >&2; return 1; }

  mkdir -p /etc/caddy/sites
  cat > "/etc/caddy/sites/$name.caddy" <<EOF
# instance: $name — generated by little-living-apps. Fronts the app (:$app_port) and the Inspector
# (:$inspector_port) for $domain. Imported by the top Caddyfile (import /etc/caddy/sites/*.caddy), so
# regenerating that file never clobbers this block.
$domain {
	# Inspector: the manager's read-only observability plane (only live when the instance runs with
	# INSPECTOR_ENABLED=true). handle_path strips /_inspect; the manager checks INSPECTOR_TOKEN.
	handle_path /_inspect/* {
		reverse_proxy 127.0.0.1:$inspector_port
	}
	reverse_proxy 127.0.0.1:$app_port
}
EOF

  # Ensure the top Caddyfile imports the sites dir — create it if this host has none yet.
  mkdir -p /etc/caddy
  if [[ ! -f /etc/caddy/Caddyfile ]]; then
    printf 'import /etc/caddy/sites/*.caddy\n' > /etc/caddy/Caddyfile
  elif ! grep -q 'import /etc/caddy/sites/' /etc/caddy/Caddyfile; then
    sed -i '1i import /etc/caddy/sites/*.caddy' /etc/caddy/Caddyfile
  fi

  if command -v caddy >/dev/null 2>&1; then
    caddy validate --config /etc/caddy/Caddyfile >/dev/null 2>&1 ||
      { echo "Caddy config invalid after adding '$name' — check /etc/caddy/sites/$name.caddy" >&2; return 1; }
    systemctl reload caddy 2>/dev/null || systemctl restart caddy
  else
    echo "WARN: caddy not installed — site block for '$name' written but not served. Install Caddy, then reload." >&2
  fi
}
